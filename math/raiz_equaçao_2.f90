PROGRAM RAIZ_EQUACAO
	IMPLICIT NONE
	REAL :: F,A,B,TOL,X,X1,X2,K,RES
	
	A=0E0
	B=0E0
	TOL=0E0
	X1=0E0
	X2=0E0
	K=0E0
	
	
	PRINT*, "QUAL O INICIO DO INTERVALO?"
	READ*, A
	
	PRINT*, "QUAL O FINAL DO INTERVALO?"
	READ*, B
	
	PRINT*, "QUAL A TOLERANCIA?"
	READ*, TOL
	
	DO
	
		X1=(A+B)/2
		
		IF ((F(X1)*F(A))>0) THEN
			PRINT*, "INTERVALO INAPROPRIADO, ESCOLHA OUTRO (EXISTE MAIS DE UMA RAIZ)"
			X1=(-1)*X2
			EXIT
		END IF
	
		IF (F(X1)*F(A)==0) THEN
			X2=X1
			EXIT
		END IF
	
		IF (F(X1)*F(A)<0) THEN
			B=X1
			ELSE
			A=X1
		END IF
	
		X2=(A+B)/2
	
		K=ABS(X1-X2)
		
		IF (K<TOL) EXIT
		
	END DO
	
	RES=(X1+X2)/2
	
	PRINT*, RES
	
	
END PROGRAM RAIZ_EQUACAO


REAL FUNCTION F(X)
	REAL :: X
	F=tan(X)-3
	RETURN
END FUNCTION F
