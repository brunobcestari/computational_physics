PROGRAM INTEGRAL
	IMPLICIT NONE
	REAL :: M,T,KB, VI, VF, V, F, I, H, P
	
	M = 2E0*14E0*1.67E0*1E-27
	T = 300
	KB = 1.38E0-23
	H=0.01
	
	
	
	PRINT*,"QUAL A VELOCIDADE INFERIOR?"
	READ*, VI
	PRINT*, "QUAL A VELOCIDADE SUPERIOR?"
	READ*, VF
	
	V=VI
	
	DO 
		
		IF (V>VF) EXIT
		
		I=I+(H/2)*(F(V)+F(V+H))
		V=V+H
		
		
	END DO
	
	P=100*I
	
	PRINT*, "A PORCENTAGEM DE PARTICULAS ENTRE",VI,"E",VF,"Ã‰:"
	PRINT*, P
		
		
END PROGRAM INTEGRAL


REAL FUNCTION F(V)
	IMPLICIT NONE
	REAL :: M,T,KB, PI=3.141592, V
	
	M = 2E0*14E0*1.67E0*1E-27
	T = 300
	KB = 1.38E-23
	
	F=SQRT((2E0/PI)*((M/(KB*T))**3E0))*(V**2)*EXP((-1)*(M*(V**2E0))/(2E0*KB*T))
	
	RETURN
	
END FUNCTION F
